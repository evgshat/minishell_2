#include "libft.h"

int	ft_isalnum(int ch)
{
	int	res;

	res = 0;
	if ((90 >= ch && ch >= 65) || (97 <= ch && ch <= 122))
		res = 1;
	if (57 >= ch && ch >= 48)
		res = 1;
	return (res);
}
